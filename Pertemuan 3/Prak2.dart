void main() {
  int counter = 0; // Deklarasi dan inisialisasi variabel counter

  // Loop while
  while (counter < 33) {
    print(counter); // Mencetak nilai counter
    counter++; // Menaikkan nilai counter
  }

  // Loop do-while
  do {
    print(counter); // Mencetak nilai counter
    counter++; // Menaikkan nilai counter
  } while (counter < 77); // Melanjutkan hingga counter kurang dari 77
}